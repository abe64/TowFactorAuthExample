const router = new Router({
  //  mode: 'hash',
    mode: 'history',
    base: process.env.BASE_URL,
    linkActiveClass: 'open active',
    scrollBehavior: () => ({ y: 0 }),
    routes: configRoutes()
  })

router.beforeEach((to, from, next) => {
  if (store.state.isTwoFactor && to.matched.some(record => record.meta.isTwoFactor)) {
    // 2段階認証中で、認証コード入力画面への遷移可
    next();
  } else if (to.matched.some(record => record.meta.isTwoFactor)) {
    // 2段階認証中ではなく認証コード入力画面へ遷移いしようとした場合はトップページへ
    next('/');
  } else if (to.matched.some(record => record.meta.isPublic) || store.state.isAuthorization) {
    // 認証不要ページ(isPublic=true) or 認証済み(isAuthorization=true) の場合は遷移可
    next()
  } else {
    // 上記以外の場合は全てログインページを表示
    next('/pages/login')
  }
})

export default router
